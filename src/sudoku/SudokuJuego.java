    private void Mostrar(int dif) {
        for(int ciclo = 0; ciclo<dif; ciclo++){
            java.util.Random random = new java.util.Random();
            int fila = random.nextInt(8);
            int columna = random.nextInt(8);
            try{
                Integer.parseInt(visual[fila][columna].getText());
            }catch(Exception e){
                visual[fila][columna].setText(Integer.toString(tablero[columna][fila]));
                visual[fila][columna].setEditable(false);
            }
        }
    }
