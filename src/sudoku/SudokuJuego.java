    private boolean Victoria(){
        try{
            for(int fil=0; fil<9; fil++){
            for(int col=0; col<9; col++){
                if(Integer.parseInt(visual[fil][col].getText()) != tablero[col][fil]){
                    return false;
                }
            }
        }
        
        return true;
        }catch(Exception e){
            return false;
        }
    }
    
    public void keyTyped(KeyEvent e) {
        int tecla = e.getKeyChar();
        boolean valido = tecla >= 48 && tecla <= 57;

        if(!valido){
            e.consume();
        }
    }
